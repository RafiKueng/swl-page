  <h2>Add your own tool</h2>
  <p>Would like to add you own tool to this list? Feel free to do so!</p>
  <p>You can provide the following information (* are mandatory):</p>
  <ul>
    <li>the title *</li>
    <li>an one line description (&lt; 80 chars, no formatting)</li>
    <li>a long description * (can contain certain html tags like &lt;h3&gt; for titles, &lt;br /&gt;, &lt;a&gt;, &lt;p&gt;)</li>
    <li>a banner image as background for the title (height: 75px, max width: 800px)</li>
    <li>a screen shot (any resolution, will be scaled down to width 380px in the overview)</li>
  </ul>
  <p>and provide links to at least one of the following items:</p>
  <ul>
    <li>the homepage</li>
    <li>a running version</li>
    <li>help  introduction page</li>
    <li>a screen cast / demo video</li>
    <li>the sources</li>
  </ul>
</div>
